const { Router } = require('express');

const userController = require('../controllers/user.controller');

const router = Router();

/**
 * @swagger
 * /login:
 *   post:
 *     summary: User logs in
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               email:
 *                 type: string
 *                 description: The user email
 *               password:
 *                 type: string
 *                 description: The user password
 *     responses:
 *       200:
 *         description: OK.
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 token:
 *                   type: string
 *                   description: User token
 */
router.post('/login', userController.login);

/**
 * @swagger
 * /register:
 *   post:
 *     summary: User registers
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               username:
 *                 type: string
 *                 description: The user username
 *               email:
 *                 type: string
 *                 description: The user email
 *               password:
 *                 type: string
 *                 description: The user password
 *     responses:
 *       200:
 *         description: OK.
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 username:
 *                   type: string
 *                   description: New user username
 *                 email:
 *                   type: string
 *                   description: New user email
 */
router.post('/register', userController.register);

module.exports = router;
